var job="Software";

switch (job){
    case "Doctor":
        console.log("I'm a doctor");
        break;
    case "Teacher":
        console.log("I'm a teacher");
        break;
    case "Software":
        console.log("I'm a software'");
        break;
    default:
        console.log("İşsizlik");
}
